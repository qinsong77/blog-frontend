            <p class="intro">
                <i>前言</i>
                <span>动迁，对于普通人来说，一直都是一个牵动人心的话题。“一夜暴富”的过程里，总有各种各样令人感慨和唏嘘的事情发生。</span>
                <span>但也只有经历过动迁的人，才知道真实的生活往往和人云亦云的传言有着极大的不同。</span>
                <span>在这个连载中，我将以一个事件亲历者的角度，和大家分享一些动迁前后发生的或喜或悲的故事，在这片曾经上海最大的棚户区的土地上，故事的主角和你我一样，是过着最平凡生活的普通人。</span>
            </p>
            <p class="desc">我家的旧址，曾在上海的最大的一片棚户区里。</p>
            <p>这片沿着黄浦江小支流而建的棚户区里的居民，若要追根溯源，九成九的人都是从外地来的，其中江苏人最多，其次是浙江人，偶尔会有一些安徽、山东的。有人家的祖辈在这里扎根的时间，比上海开埠还要早。</p>
            <p>不管是太平天国时逃难来的、抗日战争时来的，还是建国初期来的，大家共同的特征就是“逃难”，以及“没钱”。</p>
            <p>抗日战争时，上海本地和外地大批流离失所的民众移居至此，搭建起一个个窝棚简屋，棚户区初显雏形。白天人们在岸上卖菜、搬运、踩黄包车、拾荒，到了晚上，船停在河边，人就住在船上。</p>
            <p>一户人家站稳脚跟，亲朋好友便来投靠。建国前后，更多外来人口不断涌入，大家就一起住在“屋顶盖草、篱笆编墙”的草棚房里，靠煤油灯照明，用河水洗衣做饭。</p>
            <p>50年代，政府对这里进行了户籍排查后，给了逃难的人合法的户籍，又统一丈量了土地，每家每户都有了土地登记记录——自此，祖辈的身份，才从难民变成了这里真正的主人。</p>
            <p>渐渐的，当年的窝棚一步步升级成泥瓦房、钢筋水泥房，到了90年代，家家又统一办了《国有土地使用权证》<font face="楷体">（即老百姓口中的“土地证”）</font>或公有房屋租赁合同<font face="楷体">（和现在的共有产权房很相似，但承租人拥有80%产权）</font>。当然，也有不少人嫌弃这里是贫民窟，发达之后，就搬离了。</p>
            <p>时过境迁，昔日的农田和荒地也成了市中心，附近的楼盘房价从最早的两三千元逐渐涨到了快十万一平。早在十几年前，政府便启动了动迁工程，但由于涉及的人口、土地规模实在太过于庞大复杂，这一大片棚户区便被划成了近百块的零散“地块”，如同蚂蚁搬家一样，今天拆一点，明天拆一点。</p>
            <p>多年后，“蚂蚁搬家”终于搬到我们这里——这场动迁大家都期盼了很久，久到了在我出生前就已经开始有人在议论，而现在，我已经到了而立之年。</p>
            <p class="title_line">1</p>
            <p>“动迁了，终于动迁了。”一大早，父亲打来电话，说邻里间已经在奔走相告了。</p>
            <p>社区的工作人员在弄堂口的告示栏张贴出了《动迁征询告知书》，上面写着动迁的“三步走”。现在已经准备进行第一步——征询本地区居民是否愿意动迁，通过率的标准是90%，如果不到这个数，则宣告动迁失败。</p>
            <p>我住的房子离棚户区不远，当初结婚买新房时，总觉得故土难离，也考虑到要照顾还住在棚户区里的几位老人，所以也没敢买远了。</p>
            <p>撂下父亲的电话，我就赶回了棚户区凑热闹，看到了告示后，顿觉索然无味：告知书里的内容并无亮点，只是宣布动迁开始了，补偿方案、时间节点等信息，一样都没提。</p>
            <p>早上接到父亲电话一高兴，连早饭都没吃，肚子不免咕咕叫了，想都没想，抬脚便向李师傅家的馄饨铺走去。</p>
            <p>李师傅家的馄饨铺离告示栏步行只要三四分钟，自我从小记事起就已存在。铺子的特色招牌是菜肉大馄饨，虽然菜多肉少，但李师傅是我见过的少数会在平时和顾客一起吃自己做的食物的厨子。</p>
            <p>李师傅的父亲是解放前从江苏逃难来的，在这里搭了个窝棚就扎下了根，后来拿到了一纸地权凭证后便再未离开。李师傅一辈子最骄傲的事，就是80年代在纺织厂上班时做过小头头；最遗憾的事，则是他那时“高风亮节”，把原本给厂子里优先分配给他的房子让给了困难户。</p>
            <p>自从上世纪末纺织业下岗大潮之后，李师傅便靠经营这个馄饨铺维持到现在，拜当时优先照顾下岗工人的政策，小馄饨铺也是正儿八经有营业执照的。</p>
            <p>李师傅的独子小李，小时候也曾和我一起打闹过，中专毕业后在外到处换工作，似乎也不太顺利。</p>
            <p>到了地方，我照旧像以前那样，点了碗大馄饨，可能是因为邻居们都去看动迁公告了，馄饨铺显得有些冷清，李师傅手里没活儿干，就坐在了我旁边。</p>
            <p>吃了几口，我忽然觉得馄饨里的肉好像比以前多了，便说：“李师傅，是我长久没吃了，还是你家馄饨里肉确实放多了？”</p>
            <p>“是肉确实放多了，这个馄饨现在都是我儿子做了。他做才没多久，每次做出来，馅儿里的肉和菜比例都不一样。”李师傅笑着答。</p>
            <p>“那是准备子承父业了咯？”</p>
            <p>“哎。他要能找到份好工作，何必受这每天早起操劳的苦？我开了这个摊头，除了春节，平时都不休息的，他做了才没多久，能不能坚持下去还不知道呢。你上次来吃，都不知道是多久之前了。”李师傅感慨道。“你现在怎么样了？能在以前凭自己本事搬出去的，都是厉害的人，比我们这种老棺材强多了！”</p>
            <p>“哪里哪里，我搬得也不远，房子也不大。”</p>
            <p>“那也很厉害了，我现在就是担心我儿子。以前是担心他经营不了馄饨铺，现在我担心，动迁了，不管是搬进了动迁房还是自己出去买房，都肯定不会再有这么片地给我经营了，要再开，就只能花大价钱去买铺子咯，所以还是给他找个工作比较好，你那里要是有的话也帮我留意下吧。”</p>
            <p>因为李师傅持有的是《国有土地使用权证》，所以他在棚户区里的房子算“私房”，可以作为居住使用，也可以用于经营<font face="楷体">（此情况属于历史遗留问题）</font>。所以，李师傅把3层的房子分开使用，第一层开了馄饨铺，上面两层则用来居住。按照政策，动迁后置换的都是商品住宅，无法再开店铺的。</p>
            <p>“好的，我会留意的。”我当然不能明着拒绝，但我知道，小李的学历不高，要找份称心的工作，真的很难。</p>
            <p class="title_line">2</p>
            <p>动迁的告示出来后没多久，这片原本安静的棚户区就迎来了从未有过的热闹：家里子女多的，因为动迁补偿分配闹起了矛盾，屋子里能连续几小时传出不堪的叫骂声；房产中介也来了，一天发十几遍楼盘广告；还有各种正规或不正规的金融机构来发放理财产品的广告，连我家的老奶奶都抱怨，每天午睡时总会被敲门声吵醒；当然最开心的，还是废品收购站的人，能收到的废纸比以前翻了好几倍，每天都来不及捡。</p>
            <p>终于到了是否同意动迁的投票环节了，我陪着父亲去动迁办登记。动迁办公室里人手不够，登记的速度慢，我们领了排队号码之后，就坐在一旁等。一起等的都是些老邻居，有些人已经和我数年未见了，大家七嘴八舌，讨论着各种金融、地产和理财的话题，憧憬着动迁补偿拿到手后怎么让钱继续生钱。</p>
            <p>“你听说了吗？XX银行的理财产品都有10%的收益率了。”</p>
            <p>“知道，不过我家小孩说那不是银行自己出的，只是代销的，出了事银行不负责，叫我别买。”</p>
            <p>“银行卖出来的还能不负责？他要不负责我就去他们那里闹。”</p>
            <p>“曹路那里的房价已经4万了，我们的安置房现在才2万不到；嘉定西，旁边的楼盘已经3万了，这次给我们的安置房才1万多，要拿了肯定赚。”</p>
            <p>“好好较<font face="楷体">（好好的）</font>，嘉定西那个动迁房能要啊，旁边就是冒黑烟的殡仪馆……”</p>
            <p>“那侬洋盘<font face="楷体">（没经验）</font>了，有些外地人就喜欢这种房子，拿了，以后肯定会升值。”</p>
            <p>李师傅也在排号的人群里，自己一个人抽着烟，脸上写满了自信——尽管动迁还没开始，但他抽的烟已经从平时的红双喜或大前门，一下子升级到了中华了。</p>
            <p>我凑上去，跟他打招呼：“他们都噶三湖<font face="楷体">（聊天）</font>，你怎么就一个人在这里抽烟？”</p>
            <p>“你不知道啊，我那个儿子已经把未来都想好了，他准备动迁以后，去买套商铺继续经营馄饨，我看了虹口区之前的动迁方案，我们家不要房子，全部‘全货币安置’，最少也有个700万吧……我准备商铺上花个200万，<font face="楷体">（再用）</font>300万给他买个将来结婚的婚房，180万给自己和老婆买套‘老破小’住，剩下的钱还能买辆车……”李师傅用夹着烟头的手跟我比划着，“——不过有好多人对我说，全款买房不划算，最好用贷款，你帮我算算，贷款划不划算？”</p>
            <p>“这个不能一概而论的，有些人有理财的门道，用多的钱去理财是好，但你要是没那个渠道，就别想了。那些说贷款越多越好的，都是事后诸葛亮，现在调控这么严，能不跌就不错了。”我给他解释。</p>
            <p>“那也是，那我还是全款买吧。”他掐掉了烟头。</p>
            <p>我又看到了几个儿时的玩伴，忙着走过去打招呼，就没有再和李师傅聊下去。</p>
            <p>晚上，居委会的微信公众号里，公布了我们的动迁同意率是99.8%——第一步顺利迈过，从此时起，动迁就算正式启动了。</p>
